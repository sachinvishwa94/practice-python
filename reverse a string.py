name = "Sachin Vishwakarma" [::-1]
print(name)
print(len(name))

'''
In this particular example, the slice statement [::-1] is the same as [18:0:-1] which means start at position 18 
(because "Sachin Vishwakarma" has 18 characters), end at position 0, move with the step -1, negative one, which means 
one step backwards. String[start:stop:step]
'''